def add(x,y):
    """ Add x+y """
    return x + y

def multiply(x,y):
    """ multiply x * y """
    return x * y

def subtract(x,y):
    """ subtract x from y """
    return x - y

def divide(x,y):
    """ divide x by y """
    return x / y

def mod(x,y):
    """ return remainder of modulus x % y """
    return x % y

    
